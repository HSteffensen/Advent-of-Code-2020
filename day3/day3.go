type TerrainFeature int
type Terrain [][]TerrainFeature

const (
	Open TerrainFeature = iota
	Tree
)

func (tf TerrainFeature) String() string {
	charValues := [2]string{".", "#"}
	return charValues[tf]
}

func (terr Terrain) SetAt(x, y int, feature TerrainFeature) {
	terr[y][x] = feature
}

func (terr Terrain) At(x, y int) TerrainFeature {
	return terr[y][x]
}

func ReadTerrainGrid(terrain string) Terrain {
	terrainLines := strings.Fields(terrain)
	terrainGrid := make(Terrain, len(terrainLines))
	terrainMap := map[rune]TerrainFeature{'.': Open, '#': Tree}
	for y, line := range terrainLines {
		terrainGrid[y] = make([]TerrainFeature, len(line))
		for x, char := range line {
			terrainGrid.SetAt(x, y, terrainMap[char])
		}
	}

	return terrainGrid
}

func (terr Terrain) TreesOnAngle(dx, dy int) int {
	count := 0
	gridWidth := len(terr[0])
	for xPos, yPos := 0, 0; yPos < len(terr); yPos += dy {
		count += int(terr.At(xPos, yPos))
		xPos += dx
		xPos %= gridWidth
	}
	return count
}
